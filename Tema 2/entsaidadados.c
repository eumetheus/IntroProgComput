#include <stdio.h>
#include <string.h>

int main() {

    // Declarando variáveis

    int idade;  // Idade, tipo inteiro

    // Mensagem de Get para a Idade

    printf("Digite a sua idade: \n");

    // Lendo e armazenando a idade
    
    scanf("%d", &idade);

    // Imprimindo o valor da idade digitado

    printf("A sua idade é: %d \n",idade);

    return 0;

}