#include <stdio.h>

int main() {

    // Variáveis int
    
    int a = 10;
    int b = 20;

    // Imprimindo o resultado das operações relacionais

    printf("a > b: %d\n", a > b);       // Retorna 0
    printf("a < b: %d\n", a < b);       // Retorna 1
    printf("a == b: %d\n", a == b);     // Retorna 0
    printf("a != b: %d\n", a != b);     // Retorna 1

    return 0;
}